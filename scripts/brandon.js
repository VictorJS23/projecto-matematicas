function toggleMatrizMenu() {
  const matrizMenu = document.getElementById('matrizMenu');
  if (matrizMenu.style.display === 'none' || matrizMenu.style.display === '') {
    matrizMenu.style.display = 'block';
  } else {
    matrizMenu.style.display = 'none';
  }
}

